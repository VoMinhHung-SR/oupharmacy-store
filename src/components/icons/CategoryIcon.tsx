'use client'
import React from 'react'

interface CategoryIconProps {
  categorySlug: string
  className?: string
}

/**
 * Map category slugs to appropriate icons
 * Returns SVG icons based on category name
 */
export function CategoryIcon({ categorySlug, className = 'w-6 h-6' }: CategoryIconProps) {
  // Extract category name from slug for icon mapping
  const categoryName = categorySlug.split('/').pop() || categorySlug
  
  // Map category names to icons
  const getIcon = () => {
    const lowerName = categoryName.toLowerCase()
    
    // Medicine categories
    if (lowerName.includes('tim-mach') || lowerName.includes('mau')) {
      return <HeartIcon className={className} />
    }
    if (lowerName.includes('tieu-hoa') || lowerName.includes('gan-mat')) {
      return <StomachIcon className={className} />
    }
    if (lowerName.includes('ho-hap')) {
      return <LungIcon className={className} />
    }
    if (lowerName.includes('than-kinh')) {
      return <BrainIcon className={className} />
    }
    if (lowerName.includes('bo') || lowerName.includes('vitamin')) {
      return <VitaminIcon className={className} />
    }
    if (lowerName.includes('da-lieu')) {
      return <SkinIcon className={className} />
    }
    if (lowerName.includes('mat') || lowerName.includes('tai') || lowerName.includes('mui') || lowerName.includes('hong')) {
      return <EyeIcon className={className} />
    }
    if (lowerName.includes('co-xuong-khop')) {
      return <BoneIcon className={className} />
    }
    if (lowerName.includes('giam-dau') || lowerName.includes('ha-sot') || lowerName.includes('khang-viem')) {
      return <PillIcon className={className} />
    }
    if (lowerName.includes('khang-sinh') || lowerName.includes('khang-nam')) {
      return <AntibioticIcon className={className} />
    }
    if (lowerName.includes('tieu-duong')) {
      return <DiabetesIcon className={className} />
    }
    if (lowerName.includes('tiet-nieu') || lowerName.includes('sinh-duc')) {
      return <UrinaryIcon className={className} />
    }
    if (lowerName.includes('ung-thu')) {
      return <CancerIcon className={className} />
    }
    if (lowerName.includes('dich-ung')) {
      return <AllergyIcon className={className} />
    }
    if (lowerName.includes('tiem-chich') || lowerName.includes('dich-truyen')) {
      return <InjectionIcon className={className} />
    }
    if (lowerName.includes('mieng-dan') || lowerName.includes('cao-xoa') || lowerName.includes('dau')) {
      return <PatchIcon className={className} />
    }
    
    // Cosmetics categories
    if (lowerName.includes('cham-soc-da') || lowerName.includes('cham-soc-co-the')) {
      return <SkincareIcon className={className} />
    }
    if (lowerName.includes('cham-soc-toc') || lowerName.includes('da-dau')) {
      return <HairIcon className={className} />
    }
    if (lowerName.includes('trang-diem') || lowerName.includes('my-pham')) {
      return <MakeupIcon className={className} />
    }
    
    // Default icon
    return <PackageIcon className={className} />
  }
  
  return getIcon()
}

